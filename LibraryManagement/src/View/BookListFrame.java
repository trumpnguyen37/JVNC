    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int row = bookTable.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(BookListFrame.this, "Vui long chon user truoc", "Loi", JOptionPane.ERROR_MESSAGE);
        } else{
            int confirm = JOptionPane.showConfirmDialog(BookListFrame.this, "Ban chac chan muon xoa khong?");
            
            if(confirm == JOptionPane.YES_OPTION){
                String maTL = String.valueOf(bookTable.getValueAt(row, 0));
                
                bookService.deleteBook(maTL);
                
                defaultTableModel.setRowCount(0);
                setBookTableData(bookService.getAllBooks());
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

