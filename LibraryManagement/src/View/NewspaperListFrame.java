    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int row = newspaperTable.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(NewspaperListFrame.this, "Vui long chon user truoc", "Loi", JOptionPane.ERROR_MESSAGE);
        } else{
            int confirm = JOptionPane.showConfirmDialog(NewspaperListFrame.this, "Ban chac chan muon xoa khong?");
            
            if(confirm == JOptionPane.YES_OPTION){
                String maTL = String.valueOf(newspaperTable.getValueAt(row, 0));
                
                newspaperService.deleteNewspaper(maTL);
                
                defaultTableModel.setRowCount(0);
                setNewspaperTableData(newspaperService.getAllNewspaper());
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

